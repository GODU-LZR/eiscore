def replace_paragraphs(doc: Document) -> int:
    replaced = 0
    for paragraph in doc.paragraphs:
        raw = paragraph.text.strip()
        if not raw:
            continue
        normalized = norm(raw)
        for old_start, new_text in RULES:
            if normalized.startswith(norm(old_start)):
                paragraph.text = new_text
                replaced += 1
                break
    return replaced


def main() -> None:
    doc = Document(str(SRC))
    replaced = replace_paragraphs(doc)
    doc.save(str(OUT))
    print(f"saved: {OUT}")
    print(f"replaced: {replaced}")


