def apply_replacements(doc: Document) -> int:
    changed = 0
    paragraphs = doc.paragraphs
    for index, new_text in REPLACEMENTS.items():
        if index >= len(paragraphs):
            continue
        if paragraphs[index].text != new_text:
            paragraphs[index].text = new_text
            changed += 1
    return changed


